#!/usr/bin/env python3

"""
This module provides basic type conversion utilities.

It includes a function `to_str` that converts a floating-point
number to a string.
"""


def to_str(n: float) -> str:
    """
    Convert a floating-point number to its string representation.

    This function takes a floating-point number as input and returns
    its string representation.

    Parameters:
    n (float): The floating-point number to convert.

    Returns:
    str: The string representation of the input number.
    """
    return str(n)
